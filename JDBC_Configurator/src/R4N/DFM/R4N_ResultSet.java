    public R4N_ResultSet(Connection conn, String query, int type) throws SQLException {
        this.conn = conn;
        this.stmt = conn.createStatement(type, ResultSet.CONCUR_READ_ONLY);
        this.rs = this.stmt.executeQuery(query);
        while (rs.next()) {
            maxPretendRows++;
        }
        maxRealRows = maxPretendRows;
        this.rs = this.stmt.executeQuery(query);

        currentRealRow = 0;
        currentPretendRow = 0;
        args = new Object[rs.getMetaData().getColumnCount()];
        argsChanged = new boolean[args.length];
        map = new HashMap();
        deletedRows = new ArrayList();
    }

    @Override
    public boolean next() throws SQLException {
        cancelRowUpdates();

        // next
        boolean weMovedForward = rs.next();
        if (weMovedForward) {
            currentPretendRow++;
            currentRealRow++;
        }
        // ignore deleted lines
        while (weMovedForward && deletedRows.contains(currentRealRow)) {
            weMovedForward = rs.next();
            if (weMovedForward) {
                currentRealRow++;
                //currentPretendRow++;
            }
        }

        // next on inserted lines if we didnt next before
        boolean weMovedForward2 = false;
        if (!weMovedForward && currentRealRow < maxRealRows) {
            currentRealRow++;
            currentPretendRow++;
            weMovedForward2 = true;
        }

        // ignore deleted lines
        while (weMovedForward2 && deletedRows.contains(currentRealRow)) {
            weMovedForward2 = currentRealRow < maxRealRows;
            if (weMovedForward2) {
                currentRealRow++;
            }
        }

        return weMovedForward || weMovedForward2; //currentRealRow < maxRealRows;
    }

    @Override
    public void close() throws SQLException {
        rs.close();
        stmt.close();
    }

    @Override
    public Object getObject(int columnIndex) throws SQLException {
        if (map.containsKey(currentRealRow)) {
            return map.get(currentRealRow)[columnIndex - 1];
        }

        return rs.getObject(columnIndex);
    }
    
    private int getIndex(String colName) throws SQLException{
        ResultSetMetaData meta = rs.getMetaData();
       
        for(int i=1; i<=meta.getColumnCount(); i++){
            if(meta.getColumnName(i).equals(colName)){
                return i;
            }
        }
        return 0;
    }
    
