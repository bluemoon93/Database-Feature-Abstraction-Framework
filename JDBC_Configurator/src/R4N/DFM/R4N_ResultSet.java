    @Override
    public void beforeFirst() throws SQLException {
        cancelRowUpdates();
        rs.beforeFirst();
        currentRealRow = 0;
        currentPretendRow = 0;
    }

    private int getCorrectAbsolute(int min, int max) {
        int val = 0;
        for (int i = 0; i < deletedRows.size(); i++) {
            int temp = deletedRows.get(i);
            if (temp > min && temp <= max) {
                val++;
            }
        }

        return val;
    }

    @Override
    public boolean absolute(int row) throws SQLException {
        cancelRowUpdates();

        currentPretendRow = row;

        int prev = 0;
        int fix = 0;
        while ((fix = getCorrectAbsolute(prev, row)) != 0) {
            prev = row;
            row += fix;
        }

        currentRealRow = row;
        if (currentRealRow <= maxPretendRows) {
            rs.absolute(row);
        }

        System.out.println("Moving to index " + currentPretendRow + ", whose real value is " + currentRealRow);
        return currentRealRow <= maxRealRows;
    }

